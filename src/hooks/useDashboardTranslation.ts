export function useDashboardTranslation() {
  const languageContext = useOptionalLanguage();
  const currentLanguage = languageContext?.currentLanguage ?? 'en';

  const dt = (key: DashboardTranslationKey, values?: Record<string, string | number>) => {
    let text = dictionaries[currentLanguage][key] ?? en[key] ?? key;
    if (values) {
      for (const [name, value] of Object.entries(values)) {
        text = text.replaceAll(`{${name}}`, String(value));
      }
    }
    return text;
  };

  return { dt };
}
